var status;
 
function start() {
        status = -1;
        action(1, 0, 0);
}

function action(mode, type, selection) {
        if (mode == -1) {
                cm.dispose();
        } else {
                if (mode == 0 && type > 0) {
                        cm.dispose();
                        return;
                }
                if (mode == 1)
                        status++;
                else
                        status--;
    
                if(status == 0) {
                        cm.sendOk("那些决心坚定，想要创造伟大成就的冒险家啊，你们 #b最终的目的地#k 就在那扇大门后面。时间塔的本源每次只能容纳 #r一支队伍#k 同时入场。在进入大门之前请确认你的队员都已做好准备。");
                        cm.dispose();
                }
        }
}
